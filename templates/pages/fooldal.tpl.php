                   <h2><strong>Az alapítványról</strong></h2> 
				   <br>
                   <p>1999. szeptember 23-án került bejegyzésre, 1279 számon. Közhasznú, természetvédelmi tevékenységének célja első sorban védett madarak állatorvosi gyógykezelése, állatkórházi ellátása, gyógyulásuk után a szabad természetbe való visszahelyezése.</p>
                   <p>Az alapítvány célja továbbá e tevékenységekkel kapcsolatos tudományos kutatás végzése, a tevékenység bemutatása és oktatása, a feladatok ellátására intézmény, elsősorban madárkórház létrehozása és üzemeltetése.</p>
                   <p>Az alapítvány székhelyén a Hortobágyi Madárkórház, igényes csontműtétek szakszerű végzésére is alkalmas, műtővel, röntgennel, laborral és a kor színvonalának megfelelő műszerezettséggel felszerelt rendelőjében évente több száz védett madár ingyenes kezelése történik. Ezek között a szalakótától a bölömbikán át a rétisasig sokféle madár előfordul, melyek jelentős része szabadon bocsátásra kerül.</p>
                   <h2><strong>Munkánk és céljaink</strong></h2>
				   <br>
				   <p>A Hortobágyi Nemzeti Park által működtetett Górés telep funkciójának megfelelő zavartalanságát eldugottsága és nehéz megközelíthetősége biztosítja. Ugyanakkor a Hortobágyhoz való viszonylag nagy, 25 km-es távolsága miatt – melyből 5 km az év egy részében járhatatlan földút – az állatorvos számára a kijutás komoly gondot, pénzt, időt, fáradságot jelentett.</p>
                   <p>A bekerült madarak azonnali állat-egészségügyi ellátása röntgent, műtőt és laborhátteret, valamint szakszerű, folyamatos állatorvosi felügyelettel történő utókezelést igényel. Ez utóbbi napi többszöri, sokszor éjszakai állatorvosi jelenlétet feltétételez, mely a telep távolsága és az útviszonyok függvényében, az év jelentős részében való megközelíthetetlensége miatt lehetetlen.</p>
                   <p>Ezért szükséges a bekerült madarak vizsgálatait, műtéteit, utókezelését közeli, elérhető helyen, állatkórházi körülmények között végezni.</p>

